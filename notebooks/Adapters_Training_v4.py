def run_sft_adapter(domain: str, samples: list[dict], base_model_path: str):
    """SFT one LoRA adapter on base model and push to HuggingFace."""
    print("\n" + "="*60)
    print(f"SFT — {domain.upper()} adapter")
    print("="*60)

    wandb.init(
        project="adaptroute-v4",
        name=f"sft-{domain}",
        config={"stage":"sft", "domain":domain, "n":len(samples)},
        reinit=True,
    )

    tokenizer = AutoTokenizer.from_pretrained(base_model_path)
    tokenizer.pad_token    = tokenizer.eos_token
    tokenizer.padding_side = "right"

    model = AutoModelForCausalLM.from_pretrained(
        base_model_path,
        torch_dtype=torch.bfloat16,
        device_map="auto",
        attn_implementation="sdpa",
    )
    if domain == "medical":
        LORA = LoraConfig(
            r=12,  # reduced from 16
            lora_alpha=24,
            lora_dropout=0.1,  # increased dropout
            bias="none",
            task_type=TaskType.CAUSAL_LM,
            target_modules=["q_proj", "k_proj", "v_proj", "o_proj",
                          "gate_proj", "up_proj", "down_proj"],
        )
    else:
      LORA = SFT_LORA

    model = get_peft_model(model, LORA)
    model.print_trainable_parameters()

    texts    = [s["text"] for s in samples]
    split    = int(0.95 * len(texts))
    train_ds = Dataset.from_dict({"text": texts[:split]})
    eval_ds  = Dataset.from_dict({"text": texts[split:]})

    # ── SFTConfig — latest API ──────────────────────────────────────────────
    sft_cfg = SFTConfig(
        output_dir=str(OUTPUT_DIR / f"sft-{domain}"),
        num_train_epochs=2,
        per_device_train_batch_size=4,
        per_device_eval_batch_size=4,
        gradient_accumulation_steps=8,       # effective batch = 32
        learning_rate=2e-4,
        warmup_ratio=0.05,
        lr_scheduler_type="cosine",
        bf16=True,
        tf32=True,
        logging_steps=LOG_STEPS,
        eval_strategy="steps",
        eval_steps=200,
        save_strategy="steps",
        save_steps=200,
        save_total_limit=1,
        load_best_model_at_end=True,
        metric_for_best_model="eval_loss",
        report_to="wandb",
        max_length=768,                      # replaces deprecated max_seq_length
        dataset_text_field="text",           # still valid in SFTConfig
        seed=SEED,
        dataloader_num_workers=2,
        optim="adamw_torch_fused",
        gradient_checkpointing=True,
        max_grad_norm=1.0,
        gradient_checkpointing_kwargs={"use_reentrant": False},
    )

    trainer = SFTTrainer(
        model=model,
        args=sft_cfg,
        train_dataset=train_ds,
        eval_dataset=eval_ds,
        processing_class=tokenizer,          # replaces deprecated tokenizer= arg
        callbacks=[EarlyStoppingCallback(early_stopping_patience=3)],
    )
    trainer.train()

    repo = ADAPTER_REPOS[domain]
    print(f"[SFT-{domain}] Pushing adapter → {repo}")
    trainer.model.push_to_hub(repo, token=HF_TOKEN)
    tokenizer.push_to_hub(repo, token=HF_TOKEN)

    wandb.finish()
    del model, trainer
    free_memory()
    print(f"[SFT-{domain}] Done.")

