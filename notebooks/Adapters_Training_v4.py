def main():
    t0 = time.time()
    print("\n" + "█"*60)
    print("  AdaptRoute v4 — Training Pipeline Start (SFT + Gating Only)")
    print("█"*60 + "\n")

    # ── 1. Load data ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
    N = 10000
    code_s    = load_code_data(n=N)
    math_s    = load_math_data(n=N)
    medical_s = load_medical_data(n=N)
    general_s = load_general_data(n=N)
    all_s     = code_s + math_s + medical_s + general_s
    random.shuffle(all_s)
    print(f"\n[Data] Total : {len(all_s)} samples across 4 classes\n")

    # ── 2-4. SFT domain adapters on base model ━━━━━━━━━━━━━━━━━━
    for domain, samples in [("medical", medical_s)]:
        run_sft_adapter(domain, samples, BASE_MODEL_ID)
        print(f"[Timer] After SFT-{domain:<8}: {(time.time()-t0)/3600:.2f} hrs")

    # ── 5. Gating network ━━━━━━━━━━━━━━━━━━━━━━━━━
    run_gate_training(all_s)
    print(f"[Timer] After gating      : {(time.time()-t0)/3600:.2f} hrs")

    # ── Summary ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
    total = (time.time()-t0)/3600
    print("\n" + "█"*60)
    print(f"  AdaptRoute v4 — Complete in {total:.2f} hrs")
    for d, r in ADAPTER_REPOS.items():
        print(f"  {d:<8} adapter → {r}")
    print(f"  Gate          → {GATE_REPO}")
    print("█"*60 + "\n")


if __name__ == "__main__":
    main()

