import pandas as pd
from sklearn.metrics import classification_report, confusion_matrix
import seaborn as sns
import matplotlib.pyplot as plt

def evaluate_gating_network(n_samples=50):
    print(f"Evaluating Gating Network with {n_samples} samples per class...")

    # 1. Prepare test data (using the load functions we defined)
    test_data = []
    test_data.extend(load_code_data(n=n_samples))
    test_data.extend(load_math_data(n=n_samples))
    test_data.extend(load_medical_data(n=n_samples))
    test_data.extend(load_general_data(n=n_samples))

    # 2. Load Gate Model and Tokenizer
    gate_tokenizer = AutoTokenizer.from_pretrained(GATE_REPO)
    gate_model = AutoModelForSequenceClassification.from_pretrained(GATE_REPO).to(DEVICE)
    gate_model.eval()

    y_true = []
    y_pred = []

    # 3. Run Inference
    for item in test_data:
        prompt = item['prompt']
        true_label = item['label']

        inputs = gate_tokenizer(prompt, return_tensors="pt", truncation=True, max_length=128).to(DEVICE)
        with torch.no_grad():
            logits = gate_model(**inputs).logits

        pred_id = torch.argmax(logits, dim=-1).item()
        pred_label = ID2LABEL[pred_id]

        y_true.append(true_label)
        y_pred.append(pred_label)

    # 4. Results
    print("\n--- Classification Report ---")
    print(classification_report(y_true, y_pred))

    # 5. Confusion Matrix Visualization
    cm = confusion_matrix(y_true, y_pred, labels=list(LABEL2ID.keys()))
    plt.figure(figsize=(8, 6))
    sns.heatmap(cm, annot=True, fmt='d', xticklabels=LABEL2ID.keys(), yticklabels=LABEL2ID.keys(), cmap='Blues')
    plt.xlabel('Predicted')
    plt.ylabel('True')
    plt.title('Gating Network Confusion Matrix')
    plt.show()

    del gate_model
    free_memory()

# Run the evaluation
evaluate_gating_network(n_samples=50)

