def load_medical_data(n: int = 10000) -> list[dict]:
    """MedMCQA + PubMedQA (labeled)"""
    print("[Data] Loading medical ...")
    samples = []

    # MedMCQA - with proper None handling
    try:
        ds2 = load_dataset("openlifescienceai/medmcqa", split="train")
        # MedMCQA has ~194k samples, use up to 7000
        for row in ds2.shuffle(seed=SEED).select(range(min(7000, len(ds2)))):
            q = row.get("question", "")
            if q is None:
                q = ""
            q = q.strip()

            # Handle options safely
            opts = []
            for k in ["opa", "opb", "opc", "opd"]:
                val = row.get(k, "")
                if val is None:
                    val = ""
                opts.append(val)

            cop = row.get("cop", 0)
            if cop is None:
                cop = 0

            exp = row.get("exp", "")
            if exp is None:
                exp = ""
            exp = exp.strip()

            ans = opts[cop] if isinstance(cop, int) and cop < len(opts) else ""
            a = f"{ans}. {exp}" if exp else ans
            a = a.strip()

            if q and a:
                samples.append({"text": fmt(q,a), "label":"medical", "prompt":q})
    except Exception as e:
        print(f"[Data] MedMCQA warning: {e}")

    # PubMedQA with correct config - using labeled set (~1k samples)
    try:
        ds3 = load_dataset("qiaojin/PubMedQA", "pqa_labeled", split="train")
        for row in ds3:
            q = row.get("question", "")
            # PubMedQA has 'final_decision' as the answer for labeled set
            a = row.get("final_decision", "")
            if q is None:
                q = ""
            if a is None:
                a = ""
            # Also try long_answer if final_decision is empty
            if not a.strip():
                a = row.get("long_answer", "")
                if a is None:
                    a = ""
            q, a = q.strip(), a.strip()
            if q and a:
                samples.append({"text": fmt(q,a), "label":"medical", "prompt":q})
    except Exception as e:
        print(f"[Data] PubMedQA warning: {e}")

    random.shuffle(samples)
    print(f"[Data] Medical : {len(samples[:n])} samples")
    return samples[:n]


