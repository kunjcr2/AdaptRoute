def tokenize_gate(batch, tokenizer):
    return tokenizer(batch["text"], padding="max_length",
                     truncation=True, max_length=128)

def compute_gate_metrics(eval_pred):
    metric = evaluate.load("accuracy")
    logits, labels = eval_pred
    preds = np.argmax(logits, axis=-1)
    return metric.compute(predictions=preds, references=labels)


def run_gate_training(all_samples: list[dict]):
    """Train DistilBERT 4-class gating network and push to HuggingFace."""
    print("\n" + "="*60)
    print("STEP 6 — Gating Network (DistilBERT 4-class)")
    print("="*60)

    wandb.init(
        project="adaptroute-v4",
        name="gating-distilbert",
        config={"stage":"gating", "classes":list(LABEL2ID.keys()), "n":len(all_samples)},
        reinit=True,
    )

    # Use only the prompt for gating — shorter = faster at inference (~5ms)
    gate_samples = [
        {"text": s["prompt"], "label": LABEL2ID[s["label"]]}
        for s in all_samples
    ]
    random.shuffle(gate_samples)
    split    = int(0.9 * len(gate_samples))
    train_ds = Dataset.from_list(gate_samples[:split])
    eval_ds  = Dataset.from_list(gate_samples[split:])

    tokenizer = AutoTokenizer.from_pretrained(GATE_MODEL_ID)
    train_ds  = train_ds.map(lambda b: tokenize_gate(b, tokenizer), batched=True)
    eval_ds   = eval_ds.map(lambda b: tokenize_gate(b, tokenizer), batched=True)
    train_ds  = train_ds.rename_column("label", "labels")
    eval_ds   = eval_ds.rename_column("label", "labels")
    train_ds.set_format("torch", columns=["input_ids","attention_mask","labels"])
    eval_ds.set_format("torch",  columns=["input_ids","attention_mask","labels"])

    model = AutoModelForSequenceClassification.from_pretrained(
        GATE_MODEL_ID,
        num_labels=len(LABEL2ID),
        id2label=ID2LABEL,
        label2id=LABEL2ID,
    )

    gate_args = TrainingArguments(
        output_dir=str(OUTPUT_DIR / "gating"),
        num_train_epochs=5,
        per_device_train_batch_size=128,
        per_device_eval_batch_size=128,
        learning_rate=3e-5,
        warmup_ratio=0.1,
        lr_scheduler_type="cosine",
        bf16=True,
        tf32=True,
        logging_steps=LOG_STEPS,
        eval_strategy="steps",
        eval_steps=100,
        save_strategy="steps",
        save_steps=100,
        save_total_limit=1,
        load_best_model_at_end=True,
        metric_for_best_model="accuracy",
        greater_is_better=True,
        report_to="wandb",
        dataloader_num_workers=2,
        optim="adamw_torch_fused",
        seed=SEED,
    )

    trainer = Trainer(
        model=model,
        args=gate_args,
        train_dataset=train_ds,
        eval_dataset=eval_ds,
        compute_metrics=compute_gate_metrics,
        data_collator=DataCollatorWithPadding(tokenizer),
        callbacks=[EarlyStoppingCallback(early_stopping_patience=3)],
    )
    trainer.train()

    print(f"[Gate] Pushing → {GATE_REPO}")
    trainer.model.push_to_hub(GATE_REPO, token=HF_TOKEN)
    tokenizer.push_to_hub(GATE_REPO, token=HF_TOKEN)

    wandb.finish()
    del model, trainer
    free_memory()
    print("[Gate] Done.")

